const revealElements = document.querySelectorAll(".reveal");
const todayDate = document.querySelector("#today-date");
const languageButtons = document.querySelectorAll("[data-lang-btn]");
const translatableElements = document.querySelectorAll("[data-i18n]");

const setVisibleFallback = () => {
  revealElements.forEach((element) => element.classList.add("is-visible"));
};

if ("IntersectionObserver" in window) {
  const observer = new IntersectionObserver(
    (entries) => {
      entries.forEach((entry) => {
        if (entry.isIntersecting) {
          entry.target.classList.add("is-visible");
          observer.unobserve(entry.target);
        }
      });
    },
    {
      threshold: 0.18,
    }
  );

  revealElements.forEach((element) => observer.observe(element));
} else {
  setVisibleFallback();
}

const renderDate = (lang) => {
  if (!todayDate) {
    return;
  }

  const locale = lang === "vi" ? "vi-VN" : "en-GB";
  const formatter = new Intl.DateTimeFormat(locale, {
    day: "numeric",
    month: "long",
    year: "numeric",
  });

  todayDate.textContent = formatter.format(new Date());
};

const applyTranslations = (lang) => {
  const dictionary = translations[lang] || translations.en;

  document.documentElement.lang = lang;
  document.title = dictionary.documentTitle;

  translatableElements.forEach((element) => {
    const key = element.dataset.i18n;

    if (Object.prototype.hasOwnProperty.call(dictionary, key)) {
      element.textContent = dictionary[key];
    }
  });

  languageButtons.forEach((button) => {
    const isActive = button.dataset.langBtn === lang;
    button.classList.toggle("is-active", isActive);
    button.setAttribute("aria-pressed", String(isActive));
  });

  renderDate(lang);
  localStorage.setItem("portfolio-language", lang);
};

languageButtons.forEach((button) => {
  button.addEventListener("click", () => {
    applyTranslations(button.dataset.langBtn);
  });
});

const savedLanguage = localStorage.getItem("portfolio-language");
const browserLanguage = navigator.language.toLowerCase().startsWith("vi") ? "vi" : "en";
const initialLanguage =
  savedLanguage && translations[savedLanguage] ? savedLanguage : browserLanguage;

applyTranslations(initialLanguage);
